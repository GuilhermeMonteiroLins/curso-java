package operadores;

public class OperadoresBitWise {

	public static void main(String[] args) {
		int n1 = 89;
		int n2 = 60;
		System.out.println(n1 & n2);// operador logico de bit AND
		System.out.println(n1 | n2);// operador logico de bit OR
		System.out.println(n1 ^ n2);// operador logico de bit XOR ou OR-EXCLUSIVO

	}

}
